#range(stop=10), start = 0, step = 1
for i in range(10):
    print(i)

print("---------")

# range(5,21) - start = 5, stop = 21, step = 1
for i in range(5,21):
    print(i)

print("---------")

# range(2,21,2) - start = 2, stop = 21, step = 2
for i in range(2,21,2):
    print(i)

print("---------")

for i in range(10,1,-1):
    print(i)

print("---------")

for i in reversed(range(1,10)):
    print(i)
