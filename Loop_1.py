#range(5) - start = 0, stop = 5, step = 1
for i in range(5):
    print(i)

print("------------")

#range(1,15) - start = 1, stop = 15, step = 1
for i in range(1,15):
    print(i)

print("------------")

#range(2,21,2) - start = 2, stop = 21, step = 2
for i in range(2,21,2):
    print(i)

print("------------")

for i in range(10,1,-1):
    print(i)

print("------------")

for i in reversed(range(1,10)):
    print(i)
